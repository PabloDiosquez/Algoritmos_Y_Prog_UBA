// Escribir una función que permita calcular el área de un rectángulo dada su base
// y altura.

/**
 * Calcula el área de un rectángulo dada su base y altura.
 *
 * @param base La longitud de la base del rectángulo.
 * @param altura La longitud de la altura del rectángulo.
 * @return El área del rectángulo (base * altura).
 */
float area_rectangulo(float base, float altura) {
    return base * altura;
}
